#ifndef APC_LIBRARY_PAIR_H
#define APC_LIBRARY_PAIR_H

template <typename Key, typename Value>
class pair {
    using key_type = Key;
    using key_pointer = Key*;
    using key_reference = Key&;
    using key_const_reference = const Key&;

    using value_type = Value;
    using value_pointer = Value*;
    using value_reference = Value&;
    using value_const_reference = const Value&;

    
};

#endif //APC_LIBRARY_PAIR_H
